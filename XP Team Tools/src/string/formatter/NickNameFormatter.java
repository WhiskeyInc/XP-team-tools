package string.formatter;

/**
 * Nickname static formatter for strings in the messages
 * @author alberto
 */
public class NickNameFormatter {
	
	/**
	 * It formats the nickname in the right way 
	 * @param nickname
	 * @return
	 */
	public static String formatNickname(String nickname) {
		return "[" + nickname + "]:";
	}

}
